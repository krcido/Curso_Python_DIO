# Fatiamento de string
nome = "Antunes"

print(nome[0])
# Imprima o indice zero da string nome = letra G

print(nome[:5])
# Imprima do indice zero ate o indice 5

print(nome[4:0])
# Imprima do indice 4 ate o final da string

print(nome[3:6])
# Imprima do indice 3 ate o indice 6 

print(nome[2:0:2])
# imprima do indice 2 ate o final da string pulando de dois em dois

print(nome[:])
# Retona toda a string

print(nome[:-1])
# Retira o ultimo caracter da string

print(nome[::-1])
# Inverte a string de tras pra frente