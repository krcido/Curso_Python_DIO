# Operadores de comparacao

# Iguadade
saldo = 1000
saque = 500
print(saldo == saque)

# diferente de 
print(saldo != saque)

# Maior que
if(saldo > saque):
    print("Saque realizado com sucesso!")
else:
    print("O saque nao pode ser realizado.")

# Menor que
if(saque < saldo):
    print("Saque realizado com sucesso!")
else:
    print("O saque nao pode ser realizado.")

# Menor ou igual que
if(saque <= saldo):
    print("Saque realizado com sucesso!")
else:
    print("O saque nao pode ser realizado.")

# Maior ou igual que
if(saldo >= saque):
    print("Saque realizado com sucesso!")
else:
    print("O saque nao pode ser realizado.")