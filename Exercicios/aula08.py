# Operadores de atribuicao

# Simples
saldo = 500
print(saldo)

# Com incremento
saldo += 100
print(saldo)
# A variavel saldo sera adicionada de 100,portando pasara a valer 600

# Com decremento 
saldo -= 200
print(saldo)
#  A variavel saldo sera decrementada de 200,portando pasara a valer 400

# Com multiplicacao
saldo *= 2
print(saldo)
 # A variavel saldo sera multiplicada por 2, portando pasara a valer 800

# Com divisao
saldo /= 2
print(saldo)
 # A variavel saldo sera dividida por 2, portando pasara a valer 400

# Com divisao inteira
saldo //= 5
print(saldo)
 # A variavel saldo sera dividida por 5
 # porem, passara a ter como valor apenas o valor inteiro da divisao, que sera 80

 # Com modulo
saldo %= 3
print(saldo)
 # A variavel saldo sera dividida por 3, 
 # porem, passara a ter como valor apenas o resto divisao, que sera 2

 # Com exponenciacao
saldo **= 2
print(saldo)
 # A variavel saldo sera elevada a 2, portanto paassara a valer 4
