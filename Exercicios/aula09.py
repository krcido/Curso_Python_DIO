# Operadores Logicos

saldo = 1000
saque = 200
limite = 100

print(saldo >= saque)
print(saque <= limite)

# Operado AND (E)
print(saldo >= saque and saque <= limite)

# Operado OR (OU)
print(saldo >= saque or saque <= limite)

# Operado NOT (NAO)
print(not (saldo >= saque and saque <= limite))

contatos = []
print(contatos)
